const int MAX = 2e5+20, MOD = 1e9+7;
int t=1;
  
void solve(){
    ll x, y;cin>>x>>y; 
    ll k = 0;
    if(x==y){cout<<-1<<endl; return;}
    if(x<y)swap(x, y);
    for(int i = 0; i<63;++i){
        if(TEST(x, i)){
            k = SET(0, i+1);
        }
    }
    k-=x;
    cout<<k<<endl;

}
  
